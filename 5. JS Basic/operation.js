// Math operations

console.log(10 + 4);
console.log(10 - 4);
console.log(10 * 4);
console.log(10 / 4);
console.log(10 % 4); // Remainder: 2
console.log(10 % 3); // Remainder: 1

console.log(10 + 3 - 5 * 10); // -37
console.log((10 + 3 - 5) * 10); // 80

let result = (10 + 3 - 5) * 10;
result = 10 * 4;
result++; // result = result + 1
result--; // result = result - 1

result += 5; // result = result + 5
result -= 5; // result = result - 5
result /= 5; // result = result / 5
result *= 5; // result = result * 5
console.log(result);
